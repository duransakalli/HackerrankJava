package Solutions;

import java.util.Scanner;

public class MaximumDraws {
	
    public static void main(String[] args) {
        int T, N;
        Scanner sc = new Scanner(System.in);
        T = sc.nextInt();
        for (int i = 0; i < T; i++) {
            N = sc.nextInt();
            System.out.println(N+1);
        }
    }
}
